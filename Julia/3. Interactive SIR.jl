# ╔═╡ deab7d01-b6f6-4f26-8a3c-30e6b79c85d4
mutable struct Person <: AbstractAgent
    id::Int64
    status::Symbol
end

# ╔═╡ 7a30dc86-eb77-4814-a02d-f5ea2d6a7f51
